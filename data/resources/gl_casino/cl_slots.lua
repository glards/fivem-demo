function loadModels(models)
    for i=1,#models do
        local hash = models[i]
        if hash ~= nil then
            RequestModel(hash)
            while not HasModelLoaded(hash) do
                Citizen.Wait(0)
            end
        end
    end
end

local machineObjects = {}
local machineInstances = {}

local MACHINE_SLOT = 1
local MACHINE_WHEEL = 2

Citizen.CreateThread(function ()
    loadModels(machinesSlotReelHashes)
    loadModels(machinesSlotWheelHashes)
    for i=1,#slotMachines do
        -- Game checks if player is within 10.0 units of the machine to create objects
        local pos = slotMachines[i]
        local machineType = slotMachinesTypes[i]

        -- Wheel
        local wheelHash = machinesSlotWheelHashes[machineType]
        if wheelHash ~= nil then
            local wheelPos = GetObjectOffsetFromCoords(pos, wheelOffset)
            local o = CreateObject(wheelHash, wheelPos, false, false, true)
            FreezeEntityPosition(o, true)
            SetEntityCollision(o, false, false)
            SetEntityRotation(o, 0.0, 0.0, pos.w, 2, true)
            table.insert(machineObjects, o)
            table.insert(machineInstances, {
                Index = i,
                Object = o,
                Pos = wheelPos,
                Heading = pos.w,
                MachineType = machineType,
                ObjectType = MACHINE_WHEEL
            })
        end

        -- Reel
        local reelHash = machinesSlotReelHashes[machineType]
        if reelHash ~= nil then
            for j=1,3 do
                local reelPos = GetObjectOffsetFromCoords(pos, slotOffsets[j])
                local o = CreateObject(reelHash, reelPos, false, false, true)
                FreezeEntityPosition(o, true)
                SetEntityCollision(o, false, false)
                SetEntityRotation(o, 0.0, 0.0, pos.w, 2, true)
                table.insert(machineObjects, o)
                table.insert(machineInstances, {
                    Index = i,
                    Object = o,
                    Pos = reelPos,
                    Heading = pos.w,
                    MachineType = machineType,
                    ObjectType = MACHINE_SLOT
                })
            end
        end
    end

    local speed = 12.0
    local delay = 100
    local next = GetGameTimer() + delay
    local angle = 0.0

    while true do
        Citizen.Wait(0)

        if GetGameTimer() > next then
            angle = angle + speed
            if angle > 360.0 then
                angle = angle - 360.0
            end 
            next = GetGameTimer() + delay
        end

        for i=1,#machineInstances do
            local instance = machineInstances[i]
            if instance.ObjectType == MACHINE_SLOT then
                SetEntityRotation(instance.Object, angle, 0.0, instance.Heading, 2, true)
            else
                SetEntityRotation(instance.Object, 0.0, angle, instance.Heading, 2, true)
            end
            
        end
    end
end)



AddEventHandler('onResourceStop', function(resource)
    if resource == GetCurrentResourceName() then
        for i=1,#machineObjects do
            DeleteEntity(machineObjects[i])
        end
        machineObjects = {}
    end
end)
